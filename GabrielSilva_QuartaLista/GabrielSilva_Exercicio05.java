public class GabrielSilva_Exercicio05{
    public static void main(String[] args){
        for(int cont = 10; cont >= 0; cont--){// Imprimindo números de 10 a 0
            System.out.println(cont);
        }
        System.out.println("Fim.");// Imprimindo "Fim".
    }
}